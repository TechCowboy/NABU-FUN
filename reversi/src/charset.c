void init_character_set()
{
	int i, c;

	i = 0;

	// 0 = Transparent
	// 1 = Black
	// 2 = Medium Green
	// 3 = Light Green
	// 4 = Dark Blue
	// 5 = Light Blue
	// 6 = Dark Red
	// 7 = Cyan
	// 8 = Medium Red
	// 9 = Light Red
	// A = Dark Yellow
	// B = Light Yellow
	// C = Dark Green
	// D = Magenta
	// E = Gray
	// F = White

	i = 0;
	for (c = 0; c < 256; c++)
	{
		vdp_set_char(c, &character_set[i], NULL, character_color[c / 8], place_all);

		i += 8;
	}
	clr();

}

